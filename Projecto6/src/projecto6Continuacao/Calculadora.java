public class Calculadora {

    //
    //construtor vazio
    public Calculadora() {
    }
    
    //
    //metodo somar
    public double soma(double a, double b){
        return (a + b);
    }
    
    //
    //metodo subtrair
    public double subtrai(double a, double b, double c){
        return (a - b - c);
    }
    
    //
    //metodo multiplica
    public double multiplica(double a, double b, double c){
        return (a * b * c);
    }
    
    //
    //metodo divisão
    public double divisao(double a, double b, double c){
        return ( a / b / c);
    }

}